#String Formatting 

string1 = "I love %s." % "Python"
print(string1)

string2 = "I love %s and %s." % ("Python", "Javascript")
print(string2)

var = "JavaScript"
string3 = "I love %s." % var
print(string3)

#For integer
string2 = "I love %d and %i." % (10, 22)
print(string2)
#For float
string2 = "I love %d and %i and %.2f." % (10, 22, 5.99)
print(string2)